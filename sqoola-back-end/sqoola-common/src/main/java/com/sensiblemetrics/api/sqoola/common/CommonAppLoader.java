package com.sensiblemetrics.api.sqoola.common;

import org.springframework.batch.core.configuration.annotation.EnableBatchProcessing;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.autoconfigure.web.servlet.error.ErrorMvcAutoConfiguration;
import org.springframework.boot.context.properties.EnableConfigurationProperties;
import org.springframework.boot.web.servlet.support.SpringBootServletInitializer;
import org.springframework.cloud.netflix.hystrix.EnableHystrix;
import org.springframework.cloud.openfeign.EnableFeignClients;
import org.springframework.context.annotation.EnableAspectJAutoProxy;
import org.springframework.scheduling.annotation.EnableScheduling;
import springfox.documentation.swagger2.annotations.EnableSwagger2;

/**
 * Sqoola common application loader
 */
@SpringBootApplication(scanBasePackages = {"com.sensiblemetrics.api.sqoola.common"}, exclude = {ErrorMvcAutoConfiguration.class})
@EnableBatchProcessing
@EnableScheduling
@EnableFeignClients
@EnableConfigurationProperties
@EnableHystrix
@EnableSwagger2
@EnableAspectJAutoProxy(proxyTargetClass = true)
//@EnableHystrixDashboard
//@EnableAuthorizationServer
public class CommonAppLoader extends SpringBootServletInitializer {

    public static void main(final String[] args) {
        SpringApplication.run(CommonAppLoader.class, args);
    }
}
